
/**
 * this an interface that contains all the elements of a post
 *
 * @export
 * @interface Post
 */
export interface Post {

    
    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    bookname: string;
    
    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    authorname: string;


    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    activitydate: string;

    
    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    activitylog: string;


    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    review: string;
    
    /**
     *
     *
     * @memberof Post
     */
    id;

    /**
     *
     *
     * @type {string}
     * @memberof Post
     */
    username: string;


   /**
    *
    *
    * @type {string}
    * @memberof Post
    */
   userimage: string;
    }
